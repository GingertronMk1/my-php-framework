    public function handleRequest(): App
    {
        $playersAndNumbers = $this->playersAndNumbers;
        array_walk(
            $playersAndNumbers,
            fn (array &$arr) => uksort(
                $arr,
                fn (int $k1, int $k2) => intval($k1) - intval($k2)
            )
        );
        uksort(
            $playersAndNumbers,
            fn (int $k1, int $k2) => intval($k2) - intval($k1)
        );

        $highestJersey = max(
            array_map(fn (array $arr) => count($arr), $playersAndNumbers)
        );

        $groupedPlayers = [];

        foreach ($playersAndNumbers as $year => $players) {
            for ($i = 1; $i <= $highestJersey; $i++) {
                $player = isset($players[$i]) ? $players[$i] : '';
                if (! isset($groupedPlayers[$i])) {
                    $groupedPlayers[$i] = [];
                }
                $groupedPlayers[$i][$year] = $player;
            }
        }

        $topRow = array_merge(['No.'], array_keys($groupedPlayers[1]));

        $str = $this->wrapInTags($this->wrapInTags(
            implode(
                '',
                array_map(
                    fn (string $name) => $this->wrapInTags($name, 'td'),
                    $topRow
                )
            ),
            'tr'
        ),
        'thead');

        foreach ($groupedPlayers as $jerseyNumber => $players) {
            $str .= $this->wrapInTags(
                $this->wrapInTags((string) $jerseyNumber, 'th', ['class="headcol"'])
                . implode(
                    '',
                    array_map(
                        fn (string $name) => $this->wrapInTags($name, 'td'),
                        $players
                    )
                ),
                'tr'
            );
        }

        $str = $this->wrapInTags($str, 'table');
        $this->app->view = $str;
        $this->app->style = [
            'td' => 'white-space: nowrap',
            'thead' => 'font-weight: 700',
            'tr:nth-of-type(14) > *' => 'border-top-width: 0.2rem;',
            'tr:nth-of-type(18) > *' => 'border-top-width: 0.2rem;',
            'tr' => 'background-color: white',
            'tr:nth-of-type(2n)' => 'background-color: #ffbbbb'
        ];
        return $this->app;
    }
